/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */

  public class TreeNode {
      int val;
      TreeNode left;
      TreeNode right;
      TreeNode() {}
      TreeNode(int val) { this.val = val; }
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
  }
class Solution {
    public List<Double> averageOfLevels(TreeNode root) {
        Deque<TreeNode> deque=new ArrayDeque<>();
        List<Double> res=new ArrayList<>();
        if(root==null){
            return res;
        }
        deque.add(root);
        while(!deque.isEmpty()){
            List<Integer> temp=new ArrayList<>();
            int size=deque.size();
            double levelSum=0.0;
            int levelSize=deque.size();
            for(;size>0;size--){
                TreeNode pop = deque.pop();
                temp.add(pop.val);
                levelSum+=pop.val;
                if(pop.left!=null){
                    deque.add(pop.left);
                }
                if(pop.right!=null){
                    deque.add(pop.right);
                }

            }
            res.add(levelSum/levelSize);
        }
        return res;
    }
}
