/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
public class TreeNode {
      int val;
      TreeNode left;
      TreeNode right;
      TreeNode() {}
      TreeNode(int val) { this.val = val; }
      TreeNode(int val, TreeNode left, TreeNode right) {
          this.val = val;
          this.left = left;
          this.right = right;
      }
  }
class Solution {
    Map<Integer,Integer> map=new HashMap<>();
    public TreeNode buildTree(int[] preorder, int[] inorder) {
        for(int i=0;i<inorder.length;i++){
            map.put(inorder[i],i);
        }
        TreeNode node = traverse(preorder, inorder, 0, preorder.length - 1, 0, inorder.length - 1);
        return node;

    }
    public TreeNode traverse(int[] preorder, int[] inorder, int preLeft, int preRight, int inLeft, int inRight){
        if(preLeft>preRight||inLeft>inRight) return null;

        Integer mid = map.get(preorder[preLeft]);
        TreeNode node = new TreeNode(inorder[mid]);
        node.left=traverse(preorder, inorder, preLeft+1, mid-1-inLeft+1+preLeft, inLeft, mid-1);
        node.right=traverse(preorder, inorder,mid-1-inLeft+1+preLeft+1,inRight,mid+1,inRight);
        return node;
    }
}
